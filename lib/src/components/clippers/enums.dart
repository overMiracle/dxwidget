enum DxClipperSides { bottom, vertical }

/// [DxClipperMessageType] is the base has two values and tells about the type of the
/// message.
/// [DxClipperMessageType.send] is when the message is sent by you.
/// [DxClipperMessageType.receive] is when the message is received by you.
enum DxClipperMessageType { send, receive }

/// [DxClipperVerticalPosition] is the base which has two values and tells about the
/// vertical position.
/// [DxClipperVerticalPosition.top] is for the top side.
/// [DxClipperVerticalPosition.bottom] is for the bottom side.
enum DxClipperVerticalPosition { top, bottom }

/// [DxClipperHorizontalPosition] is the base which has two values and tells about the
/// horizontal position.
/// [DxClipperHorizontalPosition.left] is for the left side.
/// [DxClipperHorizontalPosition.right] is for the right side.
enum DxClipperHorizontalPosition { left, right }
